package com.mudndcapstone.server.utils.character

class CharacterMonies {
    int copper
    int silver
    int electrum
    int gold
    int platinum

    CharacterMonies() {}
    CharacterMonies(int c, int s, int e, int g, int p) {
        copper = c
        silver = s
        electrum = e
        gold = g
        platinum = p
    }
}
